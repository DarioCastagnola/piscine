int	ft_atoi(char *str)
{
	int	i;
	int	n;
	int	sign;

	sign = 0;
	n = 0;
	i = 0;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	while (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			sign++;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		n = n * 10;
		n = n + (str[i] - 48);
		i++;
	}
	if (sign % 2 == 1)
		n = -n;
	return (n);
}
